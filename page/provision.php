<div class="sub_title" style="padding-top:0px;">제 1 장 총칙</div>
<p>
제1조 (명칭): 본회의 명칭은 “대학연합레져스포츠클럽 unit”라 칭한다.</br>
제2조 (목적): 본회의 목적은 다음과 같다.</br>
              레져스포츠를 통한 회원간의 친목도모와 건전한 대학 놀이 문화의 정착.
</p>
 
<div class="sub_title">제 2 장 회원</div>
<p>
제1조 (회원자격): 본회의 회원은 서울, 경인지역 대학에 재(휴)학 중이거나 졸업한자로서 본회의 취지에 맞는 교양과 자질을 겸비한 자로 한다.</br>
</br>
제2조 (자격상실): 다음 각호에 해당하는 경우 임원회의 결의로서 경고조치 또는 회원의 자격이 상실된다.</br>
 - 회의를 통하여 결정된 본 회의 결정사유에 따르지 않거나 본회의 임원진의 지시에 따르지 않는 때</br>
 - 회비 미납한 때</br>
 - 회원 간의 정보제공과 친목도모 등에 응하지 않거나 불성실한 때</br>
 - 본회의 목적에 위배되거나 명예를 훼손하는 행위를 한 때</br>
 - 본회의 활동에 있어 타 회원들이 이질감이 들도록 그룹을 만든 때</br>
 - 하극상을 행한 때</br>
 - 준회원의 경우 총단행사 2회, 정회원의 경우 총단행사 1회를 참가하지 않은 때</br>
 - 지구대 멤버쉽 출결회칙에 미달했을 때</br>
 - 휴동 시 결정한 복귀시기를 어길 때</br>
</br>
(특칙) : 전 회원은 항시 유니트 홈페이지(unit.kr)의 각 지구대 공지사항을 확인하고 숙지하여 이에 따라 행한다. 회원의 과실 및 고의로 공지사항을 확인하지 않아 입은 손해 및 피해는 본회의 책임이 없는 것으로 간주한다.</br>
</br>
제3조 (회원명칭): 회원명칭은 신입회원, 준회원, 정회원, 우수회원, OB회원으로 나눈다.</br>
</br>
제4조 (회원기준): 회원의 기준은 다음과 같이 한다.</br>
1. 신입회원 : 매 학기 들어오는 회원</br>
2. 준 회 원 : 신입회원부터 4학기까지 활동한 회원</br>
3. 정 회 원 : 휴동(군대, 연수, 학교휴학 등) 제외 5학기 이상 활동한 회원</br>
(팀장이 명단을 올린 후 총단 측에서 검토함)</br>
4. 우수회원 : 9학기 이상 활동하였으나 졸업을 하지 않은 회원</br>
5. OB 회원 : 9학기 이상 활동한 회원 중 졸업한 회원</br>
(팀장이 명단을 올린 후 총단 측에서 검토함)</br>
</br>
제5조 (휴동기준) : 동아리 휴동의 기준은 다음과 같이 한다.</br>
1. 사유 외 휴동 불가 (사유 : 군대, 어학연수, 학교휴학)</br>
2. 필요시 증명서 제출.</br>
3. 휴동 시 복귀시기 결정 (미복귀시 제명)</br>
4. 기타 사유 발생시 총단회의에서 결정한다.</br>
</p>

<div class="sub_title">제 3 장 임원 및 임기</div>
</p>
제1조 (임원의 구성): 본회의 임원은 회장1인, 부회장, 총무부장, 기획부장, 회계부장, 지원부장, 웹 관리자로 구성한다.</br>
</br>
제2조 (임기): 본회의 임원임기는 1년으로 한다.</br>
              지구대임원의 임기는 6개월 이상이며 재임이 가능하다.</br>
</br>
제3조 (임원의 임무): 본회의 임원의 임무는 다음과 같다.</br>
                 1. 회  장 : 본회를 대표하고 회의 여부를 총괄하며 회의 시 의장의 역할을 수행한다</br>
                 2. 부회장 : 회장의 보좌하며 임원진을 관리한다.</br>
                 3. 총  무 : 행상에 필요한 모든 인원 관련 준비와 회의록 작성 및 회원 간의 연락을 담당한다.</br>
                 4. 기  획 : 행사에 필요한 예약업무와 일정 기획 등을 담당한다.</br>
                 5. 회  계 : 회계 출납 업무를 담당한다.</br>
                 6. 지  원 : 본회의 홍보 및 행사 준비 등의 임무를 총괄한다.</br>
                 7. 웹 관리자 ; 홈페이지의 모든 사항의 관리한다.</br>
※ 각 지구대 팀장은 본회의 취지를 완벽히 숙지하여야 하며, 자신의 지구대 회원들을 취지에 맞춰 이끌어 나가야 할 의무와 책임이 있다.</br>
</p>

<div class="sub_title">제 4 장 회 의</div>
<p>
제1조 (회의): 회의는 다음과 같다.</br>
   1. 정기총회 : 정기총회는 매년 2월과 8월 중에 하고 장소 및 시간은 임원진에서 결정하며 임원에게 통보한다. (정기총회 = LT)</br>
   2. 주 회의 : 주 1회 회장의 지시 하에 총무부장의 통보로 지정된 장소에서 행하여진다.</br>
   3. 행사 관련 회의 : 행사 때 마다 필요 시 소집하고 장소 및 시간은 임원진에서 결정하여 임원에게 통보한다.</br>
</br>
제2조 (회의 의사결정):</br>
                 1. 정기총회 : 한학기 계획, 임원선출, 회칙수정, 예산심의, 사업보고</br>
                 2. 주 회의 : 회원간의 정보수집 정리, 연구분석</br>
                 3. 행사 관련 회의 : 행사 준비</br>
</br>
제3조 (의결 정족수)</br>
본회의 의결은 임원 과반수의 출석과 과반수의 찬성으로 하며 과반수의 회원찬성을 득해야 한다.</br>
</p>

